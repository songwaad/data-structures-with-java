    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (register.size() != 10) {
                weight.add(Integer.valueOf(jTextField3.getText()));
                tybe.add(jComboBox1.getSelectedIndex());
                register.add((String) jTextField2.getText());
                queue.add(Q);

                jTable1.setValueAt(Q, REAR, 0);
                jTable1.setValueAt(jComboBox1.getSelectedItem(), REAR, 1);
                jTable1.setValueAt( jTextField2.getText(), REAR, 2);
                jTable1.setValueAt(Integer.valueOf(jTextField3.getText()), REAR, 3);

                Q++; REAR++;
                if (REAR == 10) {
                    REAR = 0;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Full");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter the information correctly");

        }

        jTextField2.setText("");
        jTextField3.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

