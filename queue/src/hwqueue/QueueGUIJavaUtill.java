    public boolean weightCalculator(int TYBE, int WEIGHT) {
        boolean adjust = false;
        switch (TYBE) {
            case 0:
                if (WEIGHT > 10) {
                    adjust = true;
                }
                return adjust;
            case 1:
                if (WEIGHT > 15) {
                    adjust = true;
                }
                return adjust;
            case 2:
                if (WEIGHT > 25) {
                    adjust = true;
                }
                return adjust;
            case 3:
                if (WEIGHT > 50) {
                    adjust = true;
                }
                return adjust;
        }
        return adjust;
    }

