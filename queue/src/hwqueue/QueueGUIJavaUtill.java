    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (!register.isEmpty()) {
            for (int i = 0; i < 4; i++) {
                jTable1.setValueAt("", FRONT, i);
                if (weightCalculator(tybe.peek(), weight.peek())) {
                    jLabel5.setText("Overweight There is a fine of 10,000 baht.");
                } else {
                    jLabel5.setText("Weight not exceeding");
                }
            }
            jTable2.setValueAt(queue.peek(), 0, 0);
            jTable2.setValueAt(Tybe[tybe.peek()], 0, 1);
            jTable2.setValueAt(register.peek(), 0, 2);
            jTable2.setValueAt(weight.peek(), 0, 3);
            tybe.remove();weight.remove();
            queue.remove(); register.remove();
            FRONT++;
            if (FRONT == 10) {
                    FRONT = 0;
                }
        } else
            JOptionPane.showMessageDialog(this, "Emtry");
    }//GEN-LAST:event_jButton2ActionPerformed

